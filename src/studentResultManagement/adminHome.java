    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String course=(String)jComboBox1.getSelectedItem();
        String branch=(String)jComboBox2.getSelectedItem();
        String rollno=jTextField7.getText();
        String name=jTextField8.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        String fathername=jTextField10.getText();
        try{
            //pust data in database
            Class.forName("com.mysql.jdbc.Driver"); //initializing the instance
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Dishy@1117");
            Statement st=con.createStatement();
            if(rollno.isEmpty()){
                JOptionPane.showMessageDialog(null,"Roll no cannot be empty");
            }
            else{
            //whatever we want to push in db
            st.executeUpdate("insert into student values('"+rollno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fathername+"')");
            JOptionPane.showMessageDialog(null,"Succesfully added a student");
            setVisible(false);
            new adminHome().setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Rollno already exist");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

