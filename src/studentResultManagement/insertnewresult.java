    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno=jTextField1.getText();
        String physicsmarks=jTextField2.getText();
        String chemistrymarks=jTextField3.getText();
        String mathsmarks=jTextField4.getText();
        String dbmsmarks=jTextField5.getText();
        String osmarks=jTextField6.getText();
        try{
            //pust data in database
            Class.forName("com.mysql.jdbc.Driver"); //initializing the instance
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Dishy@1117");
            Statement st=con.createStatement();
            //whatever we want to push in db
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,physicsmarks,chemistrymarks,mathsmarks,dbmsmarks,osmarks) values('"+rollno+"','"+physicsmarks+"','"+chemistrymarks+"','"+mathsmarks+"','"+dbmsmarks+"','"+osmarks+"')");
            JOptionPane.showMessageDialog(null,"Succesfully updated the result");
            setVisible(false);
            new insertnewresult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Roll no does not exist");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Result for this roll no already exist");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

